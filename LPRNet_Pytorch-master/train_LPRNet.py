# -----------------------------------------------------------------------------
# Greedy_Decode_Eval：基于贪心解码的评估函数
#
# 该函数在测试集上评估模型的准确率，使用"贪心解码"（Greedy Decoding）策略：
#   1. 对每个时间步取概率最大的类别作为预测
#   2. 去掉重复的预测字符
#   3. 去掉 blank（'-'，索引为 len(CHARS)-1）字符
#   4. 将处理后的预测结果与真实标签逐一比对，统计完全匹配的样本数
#
# 参数：
#   Net     : 已训练好的 LPRNet 模型（处于 eval 模式）
#   datasets: LPRDataLoader 测试数据集
#   args    : 命令行参数对象（提供 batch_size、num_workers 等配置）
# -----------------------------------------------------------------------------
def Greedy_Decode_Eval(Net, datasets, args):
    # -------------------------------------------------------------------------
    # 计算测试集需要多少个完整的批次（去掉余数，不足一个 batch 的样本暂不评估）
    # -------------------------------------------------------------------------
    epoch_size = len(datasets) // args.test_batch_size  # 整除部分为完整批次数

    # -------------------------------------------------------------------------
    # 创建测试集的数据加载器（打乱顺序，多进程加载，使用自定义 collate_fn）
    # -------------------------------------------------------------------------
    batch_iterator = iter(DataLoader(
        datasets, args.test_batch_size, shuffle=True,
        num_workers=args.num_workers, collate_fn=collate_fn))

    # -------------------------------------------------------------------------
    # 初始化评估指标计数器：
    #   Tp   : 预测正确的样本数（True Positive）
    #   Tn_1 : 标签长度不匹配的样本数（长度不一致，无法比对）
    #   Tn_2 : 标签长度匹配但内容错误的样本数（内容不一致）
    # -------------------------------------------------------------------------
    Tp = 0      # 正确识别的车牌数
    Tn_1 = 0    # 长度不匹配（解码长度 != 真实标签长度）的样本数
    Tn_2 = 0    # 长度匹配但内容错误的样本数

    t1 = time.time()  # 记录评估开始时间，用于计算评估速度

    # -------------------------------------------------------------------------
    # 遍历测试集的每个批次
    # -------------------------------------------------------------------------
    for i in range(epoch_size):
        # -------------------------------------------------------------------------
        # 获取一个批次的数据：images=(N,C,H,W), labels=展平标签张量, lengths=每样本标签长度列表
        # -------------------------------------------------------------------------
        images, labels, lengths = next(batch_iterator)

        # -------------------------------------------------------------------------
        # 从展平后的 labels 张量中，按照 lengths 切分出每个样本的原始标签
        # 并转换为 numpy 数组，存入 targets 列表
        # -------------------------------------------------------------------------
        start = 0          # 切分起始位置
        targets = []       # 存放每个样本的标签数组
        for length in lengths:
            # 根据长度切片：labels[start:start+length] 即为当前样本的标签
            label = labels[start:start + length]
            targets.append(label)   # 添加到目标列表
            start += length          # 更新下一个样本的起始位置

        # 将 targets 列表中的张量转换为 numpy 数组（每个元素是一维数组）
        targets = np.array([el.numpy() for el in targets])

        # -------------------------------------------------------------------------
        # 将图像张量封装为 Variable（支持自动求导的变量），并移到 GPU（如果启用 CUDA）
        # -------------------------------------------------------------------------
        if args.cuda:
            images = Variable(images.cuda())
        else:
            images = Variable(images)

        # -------------------------------------------------------------------------
        # 前向传播：将图像输入网络，获取每个时间步的类别 logit
        # 输出形状: (N, class_num, T)
        # -------------------------------------------------------------------------
        prebs = Net(images)

        # -------------------------------------------------------------------------
        # 贪心解码：对每个样本的预测 logit 进行解码
        # -------------------------------------------------------------------------

        # 将预测结果从 GPU/计算图上分离，并转为 numpy 数组
        prebs = prebs.cpu().detach().numpy()
        # 存放所有样本解码后的标签
        preb_labels = list()

        # 遍历批次中的每个样本
        for i in range(prebs.shape[0]):
            # prebs[i]: (class_num, T)，第 i 个样本的预测 logit
            preb = prebs[i, :, :]
            preb_label = list()  # 存放当前样本每个时间步的预测类别索引

            # -------------------------------------------------------------------------
            # 步骤 1：对每个时间步取概率最大的类别
            # np.argmax(preb[:, j], axis=0) = 在所有 class_num 个通道上取最大值的位置
            # preb 的形状为 (class_num, T)，按列（时间步）取 argmax
            # -------------------------------------------------------------------------
            for j in range(preb.shape[1]):
                preb_label.append(np.argmax(preb[:, j], axis=0))

            # -------------------------------------------------------------------------
            # 步骤 2：去重 + 去 blank
            # CTC 解码规则：去除相邻的重复字符，以及所有的 blank（'-'，即索引 64）
            # no_repeat_blank_label：最终的车牌识别结果（字符索引列表）
            # -------------------------------------------------------------------------
            no_repeat_blank_label = list()

            # 取第一个预测字符作为初始值
            pre_c = preb_label[0]
            # 如果第一个字符不是 blank（'-'），则将其加入结果列表
            if pre_c != len(CHARS) - 1:  # len(CHARS)-1 = 64，即 '-' 的索引
                no_repeat_blank_label.append(pre_c)

            # 遍历所有时间步的预测
            for c in preb_label:
                # -------------------------------------------------------------------------
                # 跳过两种情况：
                #   1. 当前字符与前一个字符相同（重复），如 "AA" -> "A"
                #   2. 当前字符是 blank（分隔符 CTC blank），如 "A-B" -> "AB"
                # 但注意：如果当前字符是 blank，则更新 pre_c（允许 blank 之后的字符去重）
                # -------------------------------------------------------------------------
                if (pre_c == c) or (c == len(CHARS) - 1):
                    # 如果当前字符是 blank，更新 pre_c（允许 blank 之后的字符与 blank 前的字符去重）
                    if c == len(CHARS) - 1:
                        pre_c = c
                    continue  # 跳过（不添加到结果）
                # 如果不是重复也不是 blank，则加入结果
                no_repeat_blank_label.append(c)
                pre_c = c  # 更新前一个字符

            # 将当前样本的解码结果添加到批次结果列表
            preb_labels.append(no_repeat_blank_label)

        # -------------------------------------------------------------------------
        # 步骤 3：与真实标签比对，统计准确率
        # -------------------------------------------------------------------------
        for i, label in enumerate(preb_labels):
            # -------------------------------------------------------------------------
            # 比对前先检查长度是否一致
            # 如果解码长度 != 真实标签长度，记录为 Tn_1（长度不匹配），跳过内容比对
            # -------------------------------------------------------------------------
            if len(label) != len(targets[i]):
                Tn_1 += 1  # 长度不匹配
                continue
            # -------------------------------------------------------------------------
            # 长度一致：逐元素比对预测标签与真实标签
            # 如果所有位置均相等，则预测正确（Tp++），否则记录为 Tn_2（内容错误）
            # -------------------------------------------------------------------------
            if (np.asarray(targets[i]) == np.asarray(label)).all():
                Tp += 1   # 预测正确
            else:
                Tn_2 += 1  # 长度一致但内容错误

    # -------------------------------------------------------------------------
    # 计算准确率：Tp / (Tp + Tn_1 + Tn_2)
    # 其中 Tp+Tn_1+Tn_2 = 总评估样本数
    # -------------------------------------------------------------------------
    Acc = Tp * 1.0 / (Tp + Tn_1 + Tn_2)

    # -------------------------------------------------------------------------
    # 打印评估结果：
    #   Acc    : 准确率（0~1 之间的小数）
    #   Tp     : 正确识别数
    #   Tn_1   : 长度不匹配数
    #   Tn_2   : 内容错误数
    #   Total  : 总评估样本数
    # -------------------------------------------------------------------------
    print("[Info] Test Accuracy: {} [{}:{}:{}:{}]".format(
        Acc, Tp, Tn_1, Tn_2, (Tp + Tn_1 + Tn_2)))

    t2 = time.time()  # 记录评估结束时间

    # -------------------------------------------------------------------------
    # 打印评估速度：
    #   (t2 - t1) / len(datasets): 平均每个样本的评估耗时（秒）
    #   len(datasets): 测试集总样本数
    # -------------------------------------------------------------------------
    print("[Info] Test Speed: {}s 1/{}]".format(
        (t2 - t1) / len(datasets), len(datasets)))


