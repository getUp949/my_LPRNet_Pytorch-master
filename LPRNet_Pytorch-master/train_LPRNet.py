# 第六部分：主训练函数
# =============================================================================

# -----------------------------------------------------------------------------
# train()：完整的模型训练流程
#
# 包含以下步骤：
#   1. 解析命令行参数
#   2. 创建保存目录（如不存在）
#   3. 构建 LPRNet 模型
#   4. 加载预训练权重或初始化网络权重
#   5. 配置优化器（RMSprop）和学习率调度
#   6. 创建训练集和测试集 DataLoader
#   7. 多 epoch 训练循环：
#       - 数据加载
#       - 前向传播 -> 计算 CTCLoss
#       - 反向传播 -> 参数更新
#       - 定期保存模型、评估模型
#   8. 训练结束后在测试集上做最终评估
#   9. 保存最终模型权重
# -----------------------------------------------------------------------------
def train():
    # -------------------------------------------------------------------------
    # 步骤 1：解析命令行参数
    # -------------------------------------------------------------------------
    args = get_parser()

    # -------------------------------------------------------------------------
    # T_length：LPRNet 输出序列的时间步数（固定为 18，与输入宽度和网络结构相关）
    # 注意：这里直接硬编码为 18，而不是从 args.lpr_max_len 获取
    #       18 是网络中间处理后最终的时间步数（由多次 MaxPool3d 和卷积操作决定）
    # -------------------------------------------------------------------------
    T_length = 18  # args.lpr_max_len  # （注释：此处与车牌最大长度无关，是网络的时间步输出）

    # -------------------------------------------------------------------------
    # epoch：当前已训练的 epoch 数，初始值 = 0 + resume_epoch
    #       resume_epoch > 0 时表示从中断处恢复
    # -------------------------------------------------------------------------
    epoch = 0 + args.resume_epoch

    # -------------------------------------------------------------------------
    # loss_val：累积训练损失的变量，用于统计每个 epoch 的平均损失
    # -------------------------------------------------------------------------
    loss_val = 0

    # -------------------------------------------------------------------------
    # 步骤 2：创建模型权重保存目录（如不存在）
    # -------------------------------------------------------------------------
    if not os.path.exists(args.save_folder):
        os.mkdir(args.save_folder)

    # -------------------------------------------------------------------------
    # 步骤 3：构建 LPRNet 模型
    # 根据参数构建网络实例：lpr_max_len=8, phase='train', class_num=65, dropout_rate=0.5
    # -------------------------------------------------------------------------
    lprnet = build_lprnet(
        lpr_max_len=args.lpr_max_len,
        phase=args.phase_train,
        class_num=len(CHARS),         # = 65 个字符 + 1 个 blank = 66 个类别
        dropout_rate=args.dropout_rate)

    # -------------------------------------------------------------------------
    # 选择计算设备：优先使用 CUDA GPU，否则使用 CPU
    # -------------------------------------------------------------------------
    device = torch.device("cuda:0" if args.cuda else "cpu")
    # 将模型的所有参数和张量移到指定设备（GPU/CPU）
    lprnet.to(device)
    print("Successful to build network!")

    # -------------------------------------------------------------------------
    # 步骤 4：加载预训练权重或初始化网络权重
    # -------------------------------------------------------------------------
    if args.pretrained_model:
        # 如果指定了预训练模型路径，则加载预训练权重
        # map_location='cpu': 将保存在 GPU 上的权重加载到 CPU 内存（兼容无 GPU 环境）
        lprnet.load_state_dict(torch.load(args.pretrained_model, map_location='cpu'))
        print("load pretrained model successful!")
    else:
        # -------------------------------------------------------------------------
        # 如果没有预训练模型，则手动初始化网络权重
        # 定义 Xavier 均匀初始化函数（对 weight 使用的初始化方法）
        # -------------------------------------------------------------------------
        def xavier(param):
            # nn.init.xavier_uniform: Xavier 均匀初始化，适合 ReLU 系列激活函数
            nn.init.xavier_uniform(param)

        # -------------------------------------------------------------------------
        # 定义权重初始化函数：遍历模型的所有参数，根据参数类型应用不同的初始化方法
        # -------------------------------------------------------------------------
        def weights_init(m):
            # 遍历模型状态字典中的所有键（即所有参数）
            for key in m.state_dict():
                # -------------------------------------------------------------------------
                # 如果参数的键名以 'weight' 结尾（如 layer.weight）
                # -------------------------------------------------------------------------
                if key.split('.')[-1] == 'weight':
                    # 对于卷积层（键名中含有 'conv'）：使用 Kaiming 正态初始化
                    # mode='fan_out': 适应 ReLU 激活函数（正向时 fan_out，反向时 fan_in）
                    # kaiming_normal 适合带有 ReLU 激活的网络，能更好地保持梯度方差
                    if 'conv' in key:
                        nn.init.kaiming_normal_(m.state_dict()[key], mode='fan_out')
                    # 对于 BatchNorm 层（键名中含有 'bn'）：使用 Xavier 均匀初始化
                    # 将 BatchNorm 的 weight（缩放系数 gamma）初始化为 1（即 xavier(1)）
                    if 'bn' in key:
                        m.state_dict()[key][...] = xavier(1)
                # -------------------------------------------------------------------------
                # 如果参数的键名以 'bias' 结尾（如 layer.bias）
                # 所有 bias 统一初始化为 0.01（轻微的偏置，避免完全对称）
                # -------------------------------------------------------------------------
                elif key.split('.')[-1] == 'bias':
                    m.state_dict()[key][...] = 0.01

        # -------------------------------------------------------------------------
        # 对 backbone（特征提取骨干网络）的所有子模块应用 weights_init
        # -------------------------------------------------------------------------
        lprnet.backbone.apply(weights_init)
        # 对 container（全局上下文融合层）的所有子模块应用 weights_init
        lprnet.container.apply(weights_init)
        print("initial net weights successful!")

    # -------------------------------------------------------------------------
    # 步骤 5：配置优化器
    # 使用 RMSprop（Root Mean Square Propagation）优化器：
    #   - 自适应学习率：根据参数的历史梯度自动调整每个参数的学习率
    #   - alpha=0.9: 梯度平方的衰减系数（类似于动量的平滑因子）
    #   - eps=1e-08: 为提高数值稳定性，在分母上加一个小常数
    #   - momentum=args.momentum: 动量，加速收敛，减少振荡
    #   - weight_decay: L2 正则化系数，防止过拟合
    # -------------------------------------------------------------------------
    # 注意：SGD（已注释）通常比 RMSprop 收敛更慢，但在某些场景下泛化能力更好
    # optimizer = optim.SGD(lprnet.parameters(), lr=args.learning_rate,
    #                       momentum=args.momentum, weight_decay=args.weight_decay)
    optimizer = optim.RMSprop(
        lprnet.parameters(),
        lr=args.learning_rate,
        alpha=0.9,
        eps=1e-08,
        momentum=args.momentum,
        weight_decay=args.weight_decay)

    # -------------------------------------------------------------------------
    # 步骤 6：创建训练集和测试集 DataLoader
    # -------------------------------------------------------------------------
    # 展开 "~" 等用户路径为完整的绝对路径
    train_img_dirs = os.path.expanduser(args.train_img_dirs)
    test_img_dirs = os.path.expanduser(args.test_img_dirs)

    # 创建训练数据集（自动加载目录中所有车牌图像）
    train_dataset = LPRDataLoader(
        train_img_dirs.split(','),   # 支持多目录（逗号分隔）
        args.img_size,               # 图像尺寸 [94, 24]
        args.lpr_max_len             # 车牌最大字符数 8
    )

    # 创建测试数据集
    test_dataset = LPRDataLoader(
        test_img_dirs.split(','),
        args.img_size,
        args.lpr_max_len
    )

    # -------------------------------------------------------------------------
    # 计算每个 epoch 包含的批次数：
    # epoch_size = 训练集样本总数 // 每批次样本数 = 每个 epoch 的完整批次数
    # -------------------------------------------------------------------------
    epoch_size = len(train_dataset) // args.train_batch_size

    # -------------------------------------------------------------------------
    # 计算训练总迭代次数：max_epoch * epoch_size
    # -------------------------------------------------------------------------
    max_iter = args.max_epoch * epoch_size

    # -------------------------------------------------------------------------
    # 创建 CTCLoss（Connectionist Temporal Classification Loss）
    # CTCLoss 用于处理输入序列和输出序列长度不一致的序列学习问题（如本例的车牌识别）
    #   blank=len(CHARS)-1: 将最后一个字符 '-'（索引 64）指定为 CTC blank 符号
    #                       blank 字符不参与最终输出，用于处理重复字符和分隔
    #   reduction='mean'  : 对 batch 中所有样本的损失取平均
    # -------------------------------------------------------------------------
    ctc_loss = nn.CTCLoss(blank=len(CHARS) - 1, reduction='mean')

    # -------------------------------------------------------------------------
    # 计算起始迭代数（用于从中断处恢复训练）
    # -------------------------------------------------------------------------
    if args.resume_epoch > 0:
        # 如果是从某个 epoch 恢复，则起始迭代数 = resume_epoch * epoch_size
        start_iter = args.resume_epoch * epoch_size
    else:
        start_iter = 0  # 从头开始训练

    # -------------------------------------------------------------------------
    # 步骤 7：多 epoch 训练循环
    # -------------------------------------------------------------------------
    for iteration in range(start_iter, max_iter):

        # -------------------------------------------------------------------------
        # 每个 epoch 开始时：
        #   - 创建新的批次迭代器（DataLoader）
        #   - 重置 epoch 损失累积值 loss_val
        #   - epoch 计数器加 1
        # -------------------------------------------------------------------------
        if iteration % epoch_size == 0:
            # -------------------------------------------------------------------------
            # 创建训练数据加载器（每个 epoch 都重新创建，以便打乱数据顺序）
            # -------------------------------------------------------------------------
            batch_iterator = iter(DataLoader(
                train_dataset,
                args.train_batch_size,
                shuffle=True,               # 每个 epoch 打乱数据顺序
                num_workers=args.num_workers,
                collate_fn=collate_fn
            ))
            loss_val = 0   # 重置损失累积值
            epoch += 1     # epoch 计数器加 1

        # -------------------------------------------------------------------------
        # 定期保存模型权重（每隔 save_interval 次迭代保存一次）
        # 保存格式：LPRNet_iteration_{iteration}.pth
        # -------------------------------------------------------------------------
        if iteration != 0 and iteration % args.save_interval == 0:
            torch.save(
                lprnet.state_dict(),
                args.save_folder + 'LPRNet_' + '_iteration_' + repr(iteration) + '.pth')

        # -------------------------------------------------------------------------
        # 定期在测试集上评估模型（每隔 test_interval 次迭代评估一次）
        # 调用 Greedy_Decode_Eval 使用贪心解码策略计算测试集准确率
        # -------------------------------------------------------------------------
        if (iteration + 1) % args.test_interval == 0:
            Greedy_Decode_Eval(lprnet, test_dataset, args)
            # lprnet.train()  # （注释）评估后应切换回训练模式

        start_time = time.time()  # 记录当前批次开始时间

        # -------------------------------------------------------------------------
        # 从 DataLoader 获取当前批次的数据
        #   images : (N, C, H, W) 的图像张量
        #   labels : 展平后的标签张量 (总标签数,)
        #   lengths: 该批次每个样本的标签长度列表
        # -------------------------------------------------------------------------
        images, labels, lengths = next(batch_iterator)

        # -------------------------------------------------------------------------
        # 为 CTCLoss 准备输入/目标长度元组
        #   input_lengths : 每个样本的输入时间步数（均为 T_length=18）
        #   target_lengths: 每个样本的标签长度（如 [7, 8, 6, ...]）
        # -------------------------------------------------------------------------
        input_lengths, target_lengths = sparse_tuple_for_ctc(T_length, lengths)

        # -------------------------------------------------------------------------
        # 更新学习率：根据当前 epoch 和 lr_schedule 调整优化器的学习率
        # -------------------------------------------------------------------------
        lr = adjust_learning_rate(optimizer, epoch, args.learning_rate, args.lr_schedule)

        # -------------------------------------------------------------------------
        # 将数据和标签封装为 Variable（支持自动求导），并移到 GPU（如果启用）
        # requires_grad=False: 这些是输入数据，不需要梯度，在优化器 step() 时不会被更新
        # -------------------------------------------------------------------------
        if args.cuda:
            images = Variable(images, requires_grad=False).cuda()
            labels = Variable(labels, requires_grad=False).cuda()
        else:
            images = Variable(images, requires_grad=False)
            labels = Variable(labels, requires_grad=False)

        # -------------------------------------------------------------------------
        # 前向传播：将图像输入 LPRNet，得到输出 logits
        # 输出形状: (N, class_num, T)
        # -------------------------------------------------------------------------
        logits = lprnet(images)

        # -------------------------------------------------------------------------
        # 对 logits 进行维度重排，以适配 CTCLoss 的输入要求
        # CTCLoss 要求输入格式为 (T, N, C)：
        #   T（时间步）：序列的长度方向，本例中 T=18
        #   N（Batch）：批次大小
        #   C（类别） : 字符类别数（66）
        # permute(2, 0, 1) 将 (N, C, T) -> (T, N, C)
        # -------------------------------------------------------------------------
        log_probs = logits.permute(2, 0, 1)  # for ctc loss: T x N x C

        # -------------------------------------------------------------------------
        # 对 class_num 维度（即 dim=2）应用 log_softmax，将输出转换为对数概率
        # log_softmax 比直接 softmax + log 在数值上更稳定（避免 log(0) 导致的数值下溢）
        # .requires_grad_(): 保留梯度的计算图，使 log_probs 可以参与反向传播
        # -------------------------------------------------------------------------
        log_probs = log_probs.log_softmax(2).requires_grad_()

        # -------------------------------------------------------------------------
        # 反向传播
        # -------------------------------------------------------------------------

        # 将优化器中所有参数的梯度清零（否则梯度会累积）
        optimizer.zero_grad()

        # -------------------------------------------------------------------------
        # 计算 CTCLoss：
        #   log_probs    : 形状 (T, N, C)，对数概率
        #   labels       : 形状 (总标签数,)，展平的目标标签序列
        #   input_lengths: 形状 (N,)，每个样本的输入时间步数
        #   target_lengths: 形状 (N,)，每个样本的目标标签长度
        # -------------------------------------------------------------------------
        loss = ctc_loss(log_probs, labels, input_lengths=input_lengths, target_lengths=target_lengths)

        # -------------------------------------------------------------------------
        # 如果 loss 为无穷大（数值不稳定，通常由梯度爆炸导致），跳过此次迭代
        # 避免无效的参数更新并防止程序崩溃
        # -------------------------------------------------------------------------
        if loss.item() == np.inf:
            continue

        # -------------------------------------------------------------------------
        # 反向传播：计算损失对所有可学习参数的梯度
        # -------------------------------------------------------------------------
        loss.backward()

        # -------------------------------------------------------------------------
        # 参数更新：使用优化器根据梯度更新模型参数
        # -------------------------------------------------------------------------
        optimizer.step()

        # -------------------------------------------------------------------------
        # 累积当前批次的损失值（用于后续统计每个 epoch 的平均损失）
        # -------------------------------------------------------------------------
        loss_val += loss.item()

        end_time = time.time()  # 记录当前批次结束时间

        # -------------------------------------------------------------------------
        # 每 20 次迭代打印一次训练状态（包括 epoch、迭代数、损失值、批次耗时、学习率）
        # -------------------------------------------------------------------------
        if iteration % 20 == 0:
            print(
                'Epoch:' + repr(epoch) +
                ' || epochiter: ' + repr(iteration % epoch_size) + '/' + repr(epoch_size) +
                ' || Totel iter ' + repr(iteration) +
                ' || Loss: %.4f||' % (loss.item()) +
                'Batch time: %.4f sec. ||' % (end_time - start_time) +
                'LR: %.8f' % (lr)
            )

    # -------------------------------------------------------------------------
    # 步骤 8：训练结束，在测试集上做最终评估
    # -------------------------------------------------------------------------
    print("Final test Accuracy:")
    Greedy_Decode_Eval(lprnet, test_dataset, args)

    # -------------------------------------------------------------------------
    # 步骤 9：保存最终模型的权重到磁盘
    # 文件名：Final_LPRNet_model.pth
    # 注意：只保存模型的 state_dict（参数字典），不含优化器状态
    # -------------------------------------------------------------------------
    torch.save(lprnet.state_dict(), args.save_folder + 'Final_LPRNet_model.pth')


