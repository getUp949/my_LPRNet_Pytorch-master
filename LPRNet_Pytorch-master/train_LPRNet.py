# -----------------------------------------------------------------------------
# collate_fn：自定义批次组装函数（collate function）
#
# DataLoader 默认的批处理方式不适用于变长标签的场景（如不同长度的车牌号）。
# 本函数将 Dataset 返回的多个样本（每个样本为 (图像, 标签列表, 长度)）组装成一个批次，
# 并将标签列表展平为一维数组，以适配 CTCLoss 的输入格式。
#
# 参数：
#   batch : DataLoader 从 Dataset 中取出的一个批次样本列表
#           每个元素是 __getitem__ 返回的 (Image, label, length) 三元组
#
# 返回：
#   imgs    : 堆叠后的图像张量，形状 (N, C, H, W)
#   labels  : 展平后的标签张量，形状 (所有标签总数,)，dtype=int
#   lengths : 该批次中每个样本的标签长度列表，类型为 Python list
# -----------------------------------------------------------------------------
def collate_fn(batch):
    # -------------------------------------------------------------------------
    # 分别用于存放图像、标签、长度
    # -------------------------------------------------------------------------
    imgs = []      # 存放该批次所有图像（预处理后）
    labels = []    # 存放该批次所有标签列表
    lengths = []   # 存放该批次每个样本的标签长度

    # 遍历批次中的每个样本
    for _, sample in enumerate(batch):
        # 解包：img 为预处理后的图像 numpy 数组，label 为字符索引列表，length 为标签长度
        img, label, length = sample
        # 将 numpy 数组转换为 PyTorch 张量，并添加到图像列表
        imgs.append(torch.from_numpy(img))
        # 将标签列表扩展（extend）到 labels 列表中（而非追加嵌套列表）
        labels.extend(label)
        # 将该样本的标签长度追加到 lengths 列表
        lengths.append(length)

    # -------------------------------------------------------------------------
    # 将标签列表转换为扁平的 numpy 数组，并转换为 int 类型
    # 示例：batch 中有 3 个样本，标签分别为 [0, 41]、[1, 2, 3]、[5, 6, 7, 8]
    # labels 经过 extend 后为 [0, 41, 1, 2, 3, 5, 6, 7, 8]
    # asarray 后展平为 (9,) 的 numpy 数组，flatten()
    # astype(np.int) 确保数据类型为整数（CTCLoss 要求 target 为整数索引）
    # -------------------------------------------------------------------------

    #labels = np.asarray(labels).flatten().astype(np.int)

    labels = np.asarray(labels).flatten().astype(int)
    # -------------------------------------------------------------------------
    # 返回组装好的批次数据：
    #   torch.stack(imgs, 0): 将多个 (C, H, W) 张量沿第 0 维（batch 维度）堆叠
    #                         得到 (N, C, H, W) 的批次图像张量
    #   torch.from_numpy(labels): 将扁平化的标签 numpy 数组转为 PyTorch 张量
    #   lengths: 每个样本的标签长度列表，供 CTCLoss 计算 target_lengths
    # -------------------------------------------------------------------------
    return (torch.stack(imgs, 0), torch.from_numpy(labels), lengths)


