# -----------------------------------------------------------------------------
# sparse_tuple_for_ctc：为 CTC Loss 准备稀疏元组格式的函数
#
# CTC（Connectionist Temporal Classification）损失函数要求输入/目标的长度信息
# 必须以特定格式传入。本函数将批量样本的输入时间步数 T 和每个目标的长度
# 转换为 PyTorch CTCLoss 所要求的 (input_lengths, target_lengths) 元组格式。
#
# 参数：
#   T_length : 输入序列的时间步数（每个样本相同，LPRNet 固定为 18）
#   lengths  : 每个样本的真实标签长度列表，如 [7, 8, 6, ...]
#
# 返回：
#   input_lengths : 每个样本对应的输入时间步数元组（所有样本均为 T_length）
#   target_lengths: 每个样本对应的标签长度元组（即 lengths 本身）
#
# 说明：
#   PyTorch CTCLoss 要求 input_lengths 和 target_lengths 必须是可以迭代的元组，
#   且每个元素对应一个样本的长度信息。
# -----------------------------------------------------------------------------
def sparse_tuple_for_ctc(T_length, lengths):
    # 初始化存放输入长度的列表
    input_lengths = []
    # 初始化存放目标长度的列表
    target_lengths = []

    # 遍历每个样本的长度信息
    for ch in lengths:
        # 将 T_length（输入时间步数）添加到 input_lengths 列表
        # 所有样本的输入时间步数均相同（由网络结构决定）
        input_lengths.append(T_length)
        # 将该样本的真实标签长度添加到 target_lengths 列表
        target_lengths.append(ch)

    # 将列表转换为元组返回（PyTorch CTCLoss 的要求格式）
    return tuple(input_lengths), tuple(target_lengths)


# -----------------------------------------------------------------------------
# adjust_learning_rate：根据 epoch 数调整学习率的函数
#
# 采用分段阶梯式衰减（Step Decay）策略：
#   epoch < lr_schedule[0]      -> lr = base_lr * 0.1^0 = base_lr
#   lr_schedule[0] <= epoch < lr_schedule[1] -> lr = base_lr * 0.1^1 = base_lr * 0.1
#   lr_schedule[1] <= epoch < lr_schedule[2] -> lr = base_lr * 0.1^2 = base_lr * 0.01
#   lr_schedule[2] <= epoch < lr_schedule[3] -> lr = base_lr * 0.1^3 = base_lr * 0.001
#   lr_schedule[3] <= epoch < lr_schedule[4] -> lr = base_lr * 0.1^4 = base_lr * 0.0001
#   epoch >= lr_schedule[-1]               -> lr = base_lr（不再衰减）
#
# 参数：
#   optimizer   : PyTorch 优化器实例，学习率将直接修改其 param_groups 中的 lr
#   cur_epoch   : 当前 epoch 编号（从 1 开始）
#   base_lr     : 初始学习率（如 0.1）
#   lr_schedule : 学习率衰减节点列表，如 [4, 8, 12, 14, 16]
#
# 返回：
#   lr : 当前计算出的学习率值（供打印或日志记录使用）
# -----------------------------------------------------------------------------
def adjust_learning_rate(optimizer, cur_epoch, base_lr, lr_schedule):
    """
    根据 epoch 数调整学习率，采用阶梯衰减策略
    """
    # 初始化学习率为 0（作为未找到合适区间的标记）
    lr = 0

    # 遍历学习率衰减节点列表，找到当前 epoch 所在的区间
    for i, e in enumerate(lr_schedule):
        # 如果当前 epoch 小于第 i 个衰减节点，则使用第 i 级衰减率
        if cur_epoch < e:
            lr = base_lr * (0.1 ** i)  # 0.1 的 i 次方，i 越大衰减越多
            break                       # 找到区间后立即退出循环

    # 如果遍历完所有节点仍未找到（即当前 epoch >= 最大衰减节点），
    # 则学习率保持在 base_lr（不再继续衰减）
    if lr == 0:
        lr = base_lr

    # 将计算得到的学习率应用到优化器的所有参数组中
    for param_group in optimizer.param_groups:
        param_group['lr'] = lr

    return lr  # 返回当前学习率，供日志记录等用途


